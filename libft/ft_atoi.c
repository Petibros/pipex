#include "libft.h"

int	ft_atoi(const char *nptr)
{
	int	i;
	int	total;
	int	minus;

	minus = 0;
	total = 0;
	i = 0;
	while (nptr[i] == ' ' || (nptr[i] >= 9 && nptr[i] <= 13))
	{
		i++;
	}
	if (nptr[i] == '-' || nptr[i] == '+')
	{
		if (nptr[i] == '-')
			minus = 1;
		i++;
	}
	while (nptr[i] >= '0' && nptr[i] <= '9')
	{
		total = (total * 10) + (nptr[i] - 48);
		i++;
	}
	if (minus == 1)
		return (-total);
	return (total);
}
