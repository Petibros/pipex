#include "libft.h"

char	*ft_strrchr(const char *s, int c)
{
	int	i;
	int	lens;

	lens = ft_strlen(s);
	i = lens;
	if (s[lens] == (unsigned char)c)
		return ((char *)&s[lens]);
	i--;
	while (i >= 0)
	{
		if (s[i] == (unsigned char)c)
			return ((char *)&s[i]);
		i--;
	}
	return (NULL);
}
